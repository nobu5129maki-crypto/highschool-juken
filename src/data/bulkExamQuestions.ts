/**
 * 入試対策向けのオリジナル問題を大量に追加するプール。
 * 公立・私立の「大問の型」に合わせた自作問題であり、過去問本文の転載はしていません。
 */
function q(
  id: string,
  prompt: string,
  choices: [string, string, string, string],
  correctIndex: 0 | 1 | 2 | 3,
  pastExamStyle: string,
  explanation: string,
  extra?: string | { passage?: string; explanationOnWrong?: string },
): Question {
  const o: Question = { id, prompt, choices, correctIndex, pastExamStyle, explanation };
  if (typeof extra === 'string') {
    o.passage = extra;
  } else if (extra) {
    if (extra.passage) o.passage = extra.passage;
    if (extra.explanationOnWrong) o.explanationOnWrong = extra.explanationOnWrong;
  }
  return o;
}

function readingsVm(
  prefix: string,
  startIdx: number,
  rows: readonly { w: string; y: string; d: readonly [string, string, string] }[],
): Question[] {
  return rows.map((row, i) =>
    q(
      `${prefix}-${startIdx + i}`,
      `「${row.w}」の読みとして正しいものを選びなさい。`,
      [row.y, row.d[0], row.d[1], row.d[2]],
      0,
      '語彙・読み',
      `「${row.w}」は「${row.y}」と読みます。`,
      {
        explanationOnWrong: `似た仮名の4択は、1文字だけ違う音にだまされやすい、よくあるパターンです。まちがえたら、正解「${row.y}」を心の中で3回、ゆっくり言ってみてください。\n\n【みわけ方】4語それぞれで、違いが出る一文字（濁点・小さい「っ」・行の違いなど）に丸を付け、ふたつずつ聞き比べます。語「${row.w}」の漢字は、教科書の用語集で一度だけ部首を確認すると安心です。\n\n【あと一歩】紙左に漢字「${row.w}」、右に読みだけ一行。毎日見る用の小メモにすると、同じ形に強くなります。`,
      },
    ),
  );
}

